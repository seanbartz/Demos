# Simulation parameters
t_max = 20  # Simulate for 20 seconds

# Initial conditions: [phi1, d_phi1, phi2, d_phi2]
# Starting with both pendulums at 90 degrees (horizontal) and at rest
y0 = [np.pi/2, 0, np.pi/2, 0]

# Solve
# Let's say L=1.0 and g=9.81, so omega0 = sqrt(9.81/1.0) approx 3.13
omega0 = 3.13
t, solution = solve_double_pendulum(y0, t_max, omega0)

#Plot the results
# Call the plotting function
plot_results(t, solution, omega0)

