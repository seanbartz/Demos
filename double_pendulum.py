def plot_poincare_section(t, solution):
    """
    Plots the Poincaré section for the double pendulum.
    The section is defined by phi1 = 0 with d_phi1 > 0.
    We plot phi2 vs d_phi2 at these crossings.
    """
    phi1 = solution[:, 0]
    d_phi1 = solution[:, 1]
    phi2 = solution[:, 2]
    d_phi2 = solution[:, 3]

    # Find indices where phi1 crosses zero with positive velocity
    # Crossing from negative to positive: phi1[i] < 0 and phi1[i+1] > 0
    # Also check d_phi1 > 0 to ensure positive velocity direction
    # Note: d_phi1 is angular velocity, so checking d_phi1[i] > 0 is usually sufficient for direction,
    # but strictly speaking, the sign change of phi1 defines the geometric crossing.

    # Indices where phi1 changes sign
    sign_change = (phi1[:-1] * phi1[1:]) < 0

    # Indices where it goes from negative to positive
    neg_to_pos = (phi1[:-1] < 0) & (phi1[1:] > 0)

    # Filter by velocity condition (optional but good practice for Poincaré sections)
    # We want crossings where velocity is positive.
    valid_indices = np.where(neg_to_pos & (d_phi1[:-1] > 0))[0]

    poincare_phi2 = []
    poincare_d_phi2 = []

    for i in valid_indices:
        # Linear interpolation to find the exact time t_cross where phi1 is zero
        # phi1(t) approx = phi1[i] + slope * (t - t[i])
        # 0 = phi1[i] + (phi1[i+1] - phi1[i]) / (t[i+1] - t[i]) * (t_cross - t[i])

        # Fraction of the time step
        # 0 = y1 + (y2 - y1) * fraction
        # fraction = -y1 / (y2 - y1)
        frac = -phi1[i] / (phi1[i+1] - phi1[i])

        # Interpolate phi2 and d_phi2
        val_phi2 = phi2[i] + frac * (phi2[i+1] - phi2[i])
        val_d_phi2 = d_phi2[i] + frac * (d_phi2[i+1] - d_phi2[i])

        # Map phi2 to [-pi, pi]
        val_phi2 = (val_phi2 + np.pi) % (2 * np.pi) - np.pi

        poincare_phi2.append(val_phi2)
        poincare_d_phi2.append(val_d_phi2)

    # Plot
    plt.figure(figsize=(8, 8))
    plt.scatter(poincare_phi2, poincare_d_phi2, s=2, alpha=0.6, color='purple')
    plt.title('Poincaré Section (phi1=0, d_phi1>0)')
    plt.xlabel('phi2 (rad)')
    plt.ylabel('d_phi2 (rad/s)')
    plt.grid(True)
    plt.xlim(-np.pi, np.pi)
    plt.show()

