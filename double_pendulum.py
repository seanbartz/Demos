import numpy as np
from scipy.integrate import odeint
import matplotlib.pyplot as plt

def solve_double_pendulum(initial_conditions, t_max, omega0=np.sqrt(9.81)):
    """
    Solves the double pendulum equations of motion provided.

    Equations:
    2*dd_phi1 + dd_phi2*cos(phi1-phi2) + (d_phi2)^2*sin(phi1-phi2) + 2*omega0^2*sin(phi1) = 0
    dd_phi2 + dd_phi1*cos(phi1-phi2) - (d_phi1)^2*sin(phi1-phi2) + omega0^2*sin(phi2) = 0

    Args:
        initial_conditions: list/array [phi1, d_phi1, phi2, d_phi2] (angles in rads, velocities in rad/s)
        t_max: maximum time for simulation (t_min is assumed 0)
        omega0: natural frequency of the pendulum (sqrt(g/L))

    Returns:
        t: array of time points
        solution: array of shape (len(t), 4) with columns [phi1, d_phi1, phi2, d_phi2]
    """

    # Create time array with dt < 0.05
    dt = 0.049  # Slightly less than 0.05 to be safe
    num_points = int(np.ceil(t_max / dt)) + 1
    t = np.linspace(0, t_max, num_points)

    def deriv(y, t, omega0):
        phi1, z1, phi2, z2 = y # z1 is d_phi1, z2 is d_phi2
        delta = phi1 - phi2
        omega0_sq = omega0**2

        # We need to solve for alpha1 (dd_phi1) and alpha2 (dd_phi2)
        # The system can be written as matrix equation M * [alpha1, alpha2]^T = B
        # M = [[2, cos(delta)], [cos(delta), 1]]
        # B = [ -z2^2*sin(delta) - 2*omega0^2*sin(phi1),
        #        z1^2*sin(delta) - omega0^2*sin(phi2) ]

        # Calculate the RHS terms (vector B)
        b1 = -z2**2 * np.sin(delta) - 2 * omega0_sq * np.sin(phi1)
        b2 = z1**2 * np.sin(delta) - omega0_sq * np.sin(phi2)

        # Solve the linear system using Cramer's rule for explicit formulas
        # Determinant of M = 2*1 - cos(delta)*cos(delta)
        det_M = 2 - np.cos(delta)**2

        # alpha1 = (b1*1 - b2*cos(delta)) / det
        alpha1 = (b1 - b2 * np.cos(delta)) / det_M

        # alpha2 = (2*b2 - b1*cos(delta)) / det
        alpha2 = (2 * b2 - b1 * np.cos(delta)) / det_M

        return [z1, alpha1, z2, alpha2]

    # Solve ODE
    solution = odeint(deriv, initial_conditions, t, args=(omega0,))
    return t, solution

# define a function to plot the results

def plot_results(t, solution, omega0):
    # Extract state variables
    phi1 = solution[:, 0]
    d_phi1 = solution[:, 1]
    phi2 = solution[:, 2]
    d_phi2 = solution[:, 3]

    # Create figure with 3 subplots
    fig, axs = plt.subplots(1, 3, figsize=(18, 5))

    # 1. Time Series Plot
    axs[0].plot(t, phi1, label=r'$\phi_1$ (Upper)')
    axs[0].plot(t, phi2, label=r'$\phi_2$ (Lower)', alpha=0.8)
    axs[0].set_title(rf'Motion ($\omega_0$={omega0:.2f} rad/s)')
    axs[0].set_xlabel('Time (s)')
    axs[0].set_ylabel('Angle (radians)')
    axs[0].legend()
    axs[0].grid(True)

    # 2. Phase Space Plot (Pendulum 1)
    axs[1].plot(phi1, d_phi1, color='tab:blue')
    axs[1].set_title(r'Phase Space: Upper Pendulum ($\phi_1$)')
    axs[1].set_xlabel(r'Angle $\phi_1$ (rad)')
    axs[1].set_ylabel(r'Velocity $\dot{\phi}_1$ (rad/s)')
    axs[1].grid(True)

    # 3. Phase Space Plot (Pendulum 2)
    axs[2].plot(phi2, d_phi2, color='tab:orange')
    axs[2].set_title(r'Phase Space: Lower Pendulum ($\phi_2$)')
    axs[2].set_xlabel(r'Angle $\phi_2$ (rad)')
    axs[2].set_ylabel(r'Velocity $\dot{\phi}_2$ (rad/s)')
    axs[2].grid(True)

    plt.tight_layout()
    plt.show()

